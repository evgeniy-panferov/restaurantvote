DROP TABLE IF EXISTS user_roles;
DROP TABLE IF EXISTS dishes;
DROP TABLE IF EXISTS votes;
DROP TABLE IF EXISTS restaurants;
DROP TABLE IF EXISTS users;


DROP SEQUENCE IF EXISTS users_unique_email_idx;
DROP SEQUENCE IF EXISTS user_roles_idx;
DROP SEQUENCE IF EXISTS votes_unique_date_user_id_idx;

CREATE SEQUENCE GLOBAL_SEQ_USERS AS INTEGER START WITH 100000;
CREATE SEQUENCE GLOBAL_SEQ_RESTAURANTS AS INTEGER START WITH 200000;
CREATE SEQUENCE GLOBAL_SEQ_VOTES AS INTEGER START WITH 300000;
CREATE SEQUENCE GLOBAL_SEQ_DISHES AS INTEGER START WITH 400000;

CREATE TABLE users
(
    id           INTEGER GENERATED BY DEFAULT AS SEQUENCE GLOBAL_SEQ_USERS PRIMARY KEY,
    name         VARCHAR(255)         NOT NULL,
    email        VARCHAR(255)         NOT NULL,
    password     VARCHAR(255)         NOT NULL,
    lastTimeVote TIMESTAMP            DEFAULT '1800-01-01 00:00:00'
);
CREATE UNIQUE INDEX users_unique_email_idx ON users (email);

CREATE TABLE user_roles
(
    user_id INTEGER      NOT NULL,
    role    VARCHAR(255) NOT NULL,
    CONSTRAINT user_roles_idx UNIQUE (user_id, role),
    FOREIGN KEY (user_id) REFERENCES users (id) ON DELETE CASCADE
);

CREATE TABLE restaurants
(
    id   INTEGER GENERATED BY DEFAULT AS SEQUENCE GLOBAL_SEQ_RESTAURANTS PRIMARY KEY,
    name VARCHAR(255) NOT NULL
);

CREATE TABLE dishes
(
    id            INTEGER GENERATED BY DEFAULT AS SEQUENCE GLOBAL_SEQ_DISHES PRIMARY KEY,
    restaurant_id INTEGER      NOT NULL,
    date          TIMESTAMP    DEFAULT '1800-01-01 00:00:00',
    description   VARCHAR(255) NOT NULL,
    price         INTEGER      NOT NULL,
    FOREIGN KEY (restaurant_id) REFERENCES restaurants (id) ON DELETE CASCADE
);

CREATE TABLE votes
(
    id            INTEGER GENERATED BY DEFAULT AS SEQUENCE GLOBAL_SEQ_VOTES PRIMARY KEY,
    date          DATE    NOT NULL,
    user_id       INTEGER NOT NULL,
    restaurant_id INTEGER NOT NULL,
    FOREIGN KEY (restaurant_id) REFERENCES restaurants (id) ON DELETE CASCADE,
    FOREIGN KEY (user_id) REFERENCES users (id) ON DELETE CASCADE
);
CREATE UNIQUE INDEX votes_unique_date_user_id_idx ON votes (date, user_id);